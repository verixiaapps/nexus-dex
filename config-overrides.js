module.exports = function override(config) {
  config.resolve = config.resolve || {};

  // Strip ModuleScopePlugin so aliases outside src/ resolve
  config.resolve.plugins = (config.resolve.plugins || []).filter(function (p) {
    return !p || !p.constructor || p.constructor.name !== 'ModuleScopePlugin';
  });

  // Remove ESLint plugin (broken JSX parsing in this project)
  config.plugins = (config.plugins || []).filter(function (p) {
    return !p || !p.constructor || p.constructor.name !== 'ESLintWebpackPlugin';
  });

  config.resolve.alias = Object.assign({}, config.resolve.alias || {}, {
    // @web3modal stubs
    '@web3modal/wagmi/react/config': STUB_PATH,
    '@web3modal/wagmi/react':        STUB_PATH,
    '@web3modal/wagmi':              STUB_PATH,
    '@web3modal/siwe':               STUB_PATH,
    '@web3modal/core':               STUB_PATH,
    '@web3modal/ui':                 STUB_PATH,
    '@web3modal/scaffold':           STUB_PATH,
    '@web3modal/ethereum':           STUB_PATH,
  });

  config.resolve.fallback = Object.assign({}, config.resolve.fallback || {}, NODE_POLYFILLS);

  // ── node: URI scheme aliases ──────────────────────────────────────────────
  // Webpack 5 does not handle `node:crypto`, `node:buffer`, etc. by default.
  // The new @polymarket/* packages (clob-client-v2, builder-signing-sdk 1.x)
  // use `node:` prefixed imports. Map each one to the same polyfill that the
  // bare name resolves to above.
  config.resolve.alias = Object.assign(config.resolve.alias, {
    'node:crypto':        require.resolve('crypto-browserify'),
    'node:buffer':        require.resolve('buffer'),
    'node:stream':        require.resolve('stream-browserify'),
    'node:util':          require.resolve('util'),
    'node:assert':        require.resolve('assert'),
    'node:http':          require.resolve('stream-http'),
    'node:https':         require.resolve('https-browserify'),
    'node:os':            require.resolve('os-browserify/browser'),
    'node:path':          require.resolve('path-browserify'),
    'node:process':       require.resolve('process/browser.js'),
    'node:url':           require.resolve('url'),
  });

  config.plugins = (config.plugins || []).concat([
    new webpack.ProvidePlugin({
      Buffer:  ['buffer', 'Buffer'],
      process: 'process/browser.js',
    }),
  ]);

  config.module = config.module || {};
  config.module.rules = (config.module.rules || []).concat([
    { test: /\.m?js$/, resolve: { fullySpecified: false } },
  ]);

  config.ignoreWarnings = (config.ignoreWarnings || []).concat([/Failed to parse source map/]);

  return config;
};
