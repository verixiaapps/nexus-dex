/**
* config-overrides.js -- react-app-rewired build config
*
* One job: make the build pass after we ripped out `@web3modal/wagmi` so the
* site can't show QR codes. Some leftover file in src/ still imports from
* `@web3modal/wagmi/react` (or its sub-paths). Webpack fails with
* "Module not found".
*
* Fix without hunting that file down right now:
*   1. At build time, write a tiny stub module to node_modules/.cache.
*   2. Alias every `@web3modal/wagmi*` import path to that stub.
*
* The stub exports no-op versions of every function the old API exposed
* (useWeb3Modal, createWeb3Modal, defaultWagmiConfig, etc.). Anything that
* still calls them gets a safe no-op instead of a crash. The real wallet
* connection in App.js / index.js uses wagmi's `useConnect` directly and is
* unaffected by this stub.
*
* If you later find the leftover file, replace its useWeb3Modal callsite
* with wagmi's useConnect (see App.js WalletModal for the pattern), then
* delete this whole file.
*/

const fs = require('fs');
const path = require('path');

// Polyfill list mirrors the browser shims declared in package.json. Without
// these, react-scripts 5 / webpack 5 won't auto-polyfill node core modules
// for browser bundles and Solana / EVM libs throw "Module not found".
const NODE_POLYFILLS = {
 buffer:        require.resolve('buffer'),
 process:       require.resolve('process/browser.js'),
 crypto:        require.resolve('crypto-browserify'),
 stream:        require.resolve('stream-browserify'),
 http:          require.resolve('stream-http'),
 https:         require.resolve('https-browserify'),
 os:            require.resolve('os-browserify/browser'),
 path:          require.resolve('path-browserify'),
 assert:        require.resolve('assert'),
 url:           require.resolve('url'),
 zlib:          false,
 fs:            false,
 net:           false,
 tls:           false,
 child_process: false,
};

// Write the @web3modal stub once per build. node_modules/.cache survives
// across hot reloads in dev and is rewritten clean on each production build.
const STUB_DIR  = path.resolve(__dirname, 'node_modules', '.cache', 'nexus-stubs');
const STUB_PATH = path.join(STUB_DIR, 'web3modal.js');
const STUB_CODE = `// AUTO-GENERATED by config-overrides.js -- do not edit; do not commit.
// No-op stand-in for @web3modal/wagmi so leftover imports don't break the build.
'use strict';
var noop = function () {};
var asyncNoop = function () { return Promise.resolve(); };
var hookResult = { open: asyncNoop, close: noop, isOpen: false };
function useWeb3Modal()       { return hookResult; }
function useWeb3ModalState()  { return { open: false, selectedNetworkId: undefined }; }
function useWeb3ModalEvents() { return { data: undefined, timestamp: 0 }; }
function useWeb3ModalTheme()  { return { themeMode: 'dark', setThemeMode: noop, themeVariables: {}, setThemeVariables: noop }; }
function useWeb3ModalAccount(){ return { address: undefined, isConnected: false, chainId: undefined }; }
function createWeb3Modal()    { return null; }
function defaultWagmiConfig(opts) { return { _stub: true, chains: (opts && opts.chains) || [] }; }
module.exports = {
 useWeb3Modal: useWeb3Modal,
 useWeb3ModalState: useWeb3ModalState,
 useWeb3ModalEvents: useWeb3ModalEvents,
 useWeb3ModalTheme: useWeb3ModalTheme,
 useWeb3ModalAccount: useWeb3ModalAccount,
 createWeb3Modal: createWeb3Modal,
 defaultWagmiConfig: defaultWagmiConfig,
 default: {},
 __esModule: true,
};`;

try {
 fs.mkdirSync(STUB_DIR, { recursive: true });
 fs.writeFileSync(STUB_PATH, STUB_CODE);
} catch (e) {
 // eslint-disable-next-line no-console
 console.warn('[config-overrides] could not write web3modal stub:', e && e.message);
}

module.exports = function override(config) {
 config.resolve = config.resolve || {};

 // CRA installs ModuleScopePlugin which blocks any import resolving outside
 // src/. Our @web3modal stub lives in node_modules/.cache, so we have to
 // strip that plugin or the alias gets rejected with
 // "Relative imports outside of src/ are not supported."
 config.resolve.plugins = (config.resolve.plugins || []).filter(function (p) {
   return !p || !p.constructor || p.constructor.name !== 'ModuleScopePlugin';
 });

 // Disable ESLintWebpackPlugin during build. CRA 5 wires it up to use
 // @babel/eslint-parser so JSX in .js files is recognized, but in this
 // project that wiring is broken -- ESLint defaults to espree, which throws
 // "Syntax error: Missing semicolon" on every JSX node. The actual JS is
 // parsed independently by babel-loader (which works fine), so removing
 // the eslint plugin from the build pipeline only removes an in-build
 // linter, not any real validation. Lint locally / in CI separately if
 // you want lint output.
 config.plugins = (config.plugins || []).filter(function (p) {
   return !p || !p.constructor || p.constructor.name !== 'ESLintWebpackPlugin';
 });

 config.resolve.alias = Object.assign({}, config.resolve.alias || {}, {
   // Every documented import path of @web3modal/wagmi -> stub
   '@web3modal/wagmi/react/config': STUB_PATH,
   '@web3modal/wagmi/react':        STUB_PATH,
   '@web3modal/wagmi':              STUB_PATH,
   // Cousin packages that may also lurk in node_modules
   '@web3modal/siwe':               STUB_PATH,
   '@web3modal/core':               STUB_PATH,
   '@web3modal/ui':                 STUB_PATH,
   '@web3modal/scaffold':           STUB_PATH,
 });
 config.resolve.fallback = Object.assign({}, config.resolve.fallback || {}, NODE_POLYFILLS);

 // ProvidePlugin so libs that grab `Buffer` / `process` off globals work in
 // the browser. Mirrors the standard CRA-with-Solana setup.
 const webpack = require('webpack');
 config.plugins = (config.plugins || []).concat([
   new webpack.ProvidePlugin({
     Buffer:  ['buffer', 'Buffer'],
     process: 'process/browser.js',
   }),
 ]);

 // .mjs interop -- some viem / wagmi sub-deps ship dual ESM/CJS and webpack
 // 5 needs `.mjs` resolved with `fullySpecified: false` to avoid extension
 // mismatch errors at build.
 config.module = config.module || {};
 config.module.rules = (config.module.rules || []).concat([
   { test: /\.m?js$/, resolve: { fullySpecified: false } },
 ]);

 // Source-map-loader can choke on missing source maps inside node_modules
 // (e.g., older bs58 / superstruct builds). Drop the warnings.
 config.ignoreWarnings = (config.ignoreWarnings || []).concat([/Failed to parse source map/]);

 return config;
};
